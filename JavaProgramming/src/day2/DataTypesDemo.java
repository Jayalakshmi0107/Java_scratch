package day2;

public class DataTypesDemo {

	public static void main(String[] args) {
	//	int a;   // declaration
	// a=100; // assignment . no need to declare again
		
		int a=100; //declaration and assignment
		System.out.println("a"); //string a
		System.out.println('a'); //char a
		System.out.println(a); //int a
		a=200; //change the a value as 200, no need to declare again but can change the value any time
		System.out.println(a);
		
		/* int a=10;
		int b=20;
		int c=30; */
		int x=10,y=20,z=30; // single line multiple values same data type
		
		System.out.println(x);
		System.out.println(y);
		System.out.println(z);
	}

}
