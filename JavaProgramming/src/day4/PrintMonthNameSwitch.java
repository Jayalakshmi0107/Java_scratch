package day4;

public class PrintMonthNameSwitch {

	public static void main(String[] args) {
		int month_no=7;
		switch(month_no)
		{
		case 1 : System.out.println("Month Name is JANUARY"); break;
		case 2 : System.out.println("Month Name is FEBRUARY"); break;
		case 3 : System.out.println("Month Name is MARCH"); break;
		case 4 : System.out.println("Month Name is APRIL"); break;
		case 5 : System.out.println("Month Name is MAY"); break;
		case 6 : System.out.println("Month Name is JUNE"); break;
		case 7 : System.out.println("Month Name is JULY");  break;
		case 8 : System.out.println("Month Name is AUGUST"); break;
		case 9 : System.out.println("Month Name is SEPTEMBER"); break;
		case 10 : System.out.println("Month Name is OCTOBER"); break;
		case 11 : System.out.println("Month Name is NOVEMBER"); break;
		case 12 : System.out.println("Month Name is DECEMBER"); break;
		default : System.out.println("Invalid Input");
		}

	}

}
